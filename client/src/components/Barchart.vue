<script>
import { Bar } from 'vue-chartjs'

export default {
  extends: Bar,
  props: {
    chartData: {
      type: Array,
      required: true
    },
    chartLabels: {
        type: Array,
        required: true
    }
  },
  data () {
      return {
        options: {
          scales: {
            yAxes: [{
              ticks: {
                beginAtZero: true
              },
              gridLines: {
                display: true
              }
            }],
            xAxes: [ {
              gridLines: {
                display: false
              }
            }]
          },
          legend: {
            display: false
          },
          responsive: true,
          maintainAspectRatio: false
        }
      }
  },
  mounted () {
     this.renderChart({
        labels: this.chartLabels,
        datasets: [
          {
            borderColor: '#289062',
            borderWidth: 1,
            backgroundColor: '#289062',
            data: this.chartData
          }
        ]
      },
    this.options)
  }
}
</script>

